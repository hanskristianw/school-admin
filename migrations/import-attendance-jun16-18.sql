SELECT t.pin, COUNT(*) AS total_scan,
  u.user_id,
  u.user_nama_depan || ' ' || u.user_nama_belakang AS nama
FROM tmp_absen_import2 t
LEFT JOIN users u ON u.user_pin = t.pin::text
GROUP BY t.pin, u.user_id, u.user_nama_depan, u.user_nama_belakang
ORDER BY t.pin;

-- PIN tidak ditemukan:
SELECT DISTINCT t.pin, COUNT(*) AS total
FROM tmp_absen_import2 t
LEFT JOIN users u ON u.user_pin = t.pin::text
WHERE u.user_id IS NULL
GROUP BY t.pin ORDER BY t.pin;

-- ============================================================
-- LANGKAH 3: IMPORT (WHERE NOT EXISTS = aman dari duplikat)
-- ============================================================
INSERT INTO attendances (user_id, scan_time, status_scan)
SELECT
  u.user_id,
  (t.tanggal::text || ' ' || t.waktu::text || '+07:00')::timestamptz AS scan_time,
  NULL AS status_scan
FROM tmp_absen_import2 t
INNER JOIN users u ON u.user_pin = t.pin::text
WHERE NOT EXISTS (
  SELECT 1 FROM attendances a
  WHERE a.user_id = u.user_id
    AND a.scan_time = (t.tanggal::text || ' ' || t.waktu::text || '+07:00')::timestamptz
);

