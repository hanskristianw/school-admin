WHERE u.user_id IS NULL
GROUP BY t.pin ORDER BY t.pin;

-- ============================================================
-- LANGKAH 3: IMPORT
-- ON CONFLICT DO NOTHING = aman dari:
--   1. Duplikat di dalam temp table (misal pin 105 Jun-19 jam sama)
--   2. Data sudah ada di DB dari import sebelumnya (Jun-18)
-- ============================================================
INSERT INTO attendances (user_id, scan_time, status_scan)
SELECT DISTINCT
  u.user_id,
  (t.tanggal::text || ' ' || t.waktu::text || '+07:00')::timestamptz AS scan_time,
  NULL AS status_scan
FROM tmp_jun1823 t
INNER JOIN users u ON u.user_pin = t.pin::text
ON CONFLICT (user_id, scan_time) DO NOTHING;

